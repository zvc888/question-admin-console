<template>
  <div class="login-container">
  </div>
</template>
<script>
export default {
  name: 'Login',
  data() {
    return {}
  },
  mounted() {
    var ticket= this.$route.query.sso_service_ticket;
    this.$store.dispatch('LoginERP', ticket).then(() => {
      this.$router.push({ path:'/' })
    }).catch((err) => {
      console.log(err)
      this.$router.push({ path:'/login' })
    })
  }
}
</script>